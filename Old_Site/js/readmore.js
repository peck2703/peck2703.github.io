// JavaScript Document
function myAlienReadFunction(){
	var dots = document.getElementById("dots-alien");
	var moreText = document.getElementById("more-alien");
	var btnText = document.getElementById("readBtn-alien");
	
	if(dots.style.display === "none"){
		dots.style.display = "inline";
		btnText.style.display = "Read Less";
		moreText.style.display = "none";
	}
	else{
		dots.style.display = "none";
		btnText.style.display = "Read Less";
		moreText.style.display = "inline";
	}
}

